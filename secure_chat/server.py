# AuthenticationChallenge holds the random byte sequence generated by the
# server during secure login. The client uses this challenge to compute an
# HMAC response with a password-derived key, allowing the server to verify
# the client's knowledge of the correct password without receiving the
# password directly.
@dataclass
class AuthenticationChallenge:
    challenge_bytes:bytes

# AuthenticationResult represents the final outcome of the secure login
# process. It indicates whether authentication succeeded, provides a
# descriptive result message, and specifies whether the subscribed
# channel is available and whether the channel keys were successfully
# loaded for secure communication.
@dataclass
class AuthenticationResult:
    status:AuthStatus
    message:str
    channel_available:bool
    channel_keys_loaded:bool=False

# ChannelKeySet stores the symmetric cryptographic material required for
# secure communication within a channel. It includes the AES encryption key,
# CBC initialization vector, HMAC key, and a flag indicating whether the
# keys are currently loaded and ready to use.
@dataclass
class ChannelKeySet:
    aes_key:bytes
    iv:bytes
    hmac_key:bytes
    keys_loaded:bool=False

# RegistrationRequestMessage is the transport-level message used during
# the enrollment phase. It contains the message type identifier and the
# RSA-encrypted registration payload, which includes the username,
# password-derived hashes, and selected channel.
@dataclass
class RegistrationRequestMessage:
    message_type:MessageType
    encrypted_payload:bytes

# RegistrationResponseMessage is the transport-level response sent by the
# server after processing a client's enrollment request. It includes the
# result code, descriptive message, and a digital signature so the client
# can verify the authenticity and integrity of the registration result.
@dataclass
class RegistrationResponseMessage:
    message_type:MessageType # Identifies this packet as a registration response
    result_code:str # High-level result such as SUCCESS or FAILURE
    result_message:str
    signature:bytes # Server's digital signature over the result content

# AuthenticationRequestMessage is the first protocol message in the secure
# login process. It contains the message type and the client's username,
# allowing the server to identify the enrolled user and begin the
# challenge-response authentication sequence.
@dataclass
class AuthenticationRequestMessage:
    message_type:MessageType # Identifies this packet as an authentication request
    username:str # Username of the client attempting to log in

# AuthenticationChallengeMessage is the protocol packet sent from the
# server to the client during secure login. It carries the random challenge
# that the client must process with a password-derived HMAC key in order
# to prove knowledge of the correct password.
@dataclass
class AuthenticationChallengeMessage:
    message_type:MessageType
    challenge:bytes # Server-generated random challenge used in secure login

# AuthenticationResponseMessage is the protocol packet sent from the client
# to the server during challenge-response authentication. It contains the
# message type and the HMAC computed over the server's random challenge
# using a key derived from the client's password.
@dataclass
class AuthenticationResponseMessage:
    message_type:MessageType
    hmac_response:bytes # HMAC over the server challenge using a password-derived key

# AuthenticationResultMessage is the final server-to-client message in the
# secure login protocol. It contains the AES-encrypted authentication result
# and the server's RSA signature over that encrypted content, allowing the
# client to verify authenticity and safely recover the result.
@dataclass
class AuthenticationResultMessage:
    message_type:MessageType
    encrypted_result:bytes # AES-CBC encrypted authentication outcome and optional channel keys
    signature:bytes # Server's RSA signature over the encrypted result

# SecureMessagePacket represents a protected channel message in the
# secure chat application. It contains the AES-encrypted message text,
# the HMAC used for integrity verification, and optionally the sender's
# username for GUI display purposes.
@dataclass
class SecureMessagePacket:
    message_type:MessageType
    ciphertext:bytes # AES-CBC encrypted message content
    hmac:bytes # HMAC over the ciphertext for integrity verification
    sender_username:str = "" # Optional sender name used for message display in the GU

# DisconnectMessage is the protocol structure used when a client or server
# wants to terminate a session intentionally. It includes the message type
# and a descriptive reason so the receiving side can perform cleanup and
# report the disconnection clearly.
@dataclass
class DisconnectMessage:
    message_type:MessageType
    reason:str # Human-readable explanation for the disconnection
# ConnectionLostEvent represents an unexpected interruption in the
# client-server communication channel. It is used to report abnormal
# disconnection cases, such as server shutdown or socket failure,
# together with a reason and optional diagnostic details.
@dataclass
class ConnectionLostEvent:
    message_type:MessageType
    reason:str # Short explanation of why the connection was lost
    details:str="" #Optional extra information




